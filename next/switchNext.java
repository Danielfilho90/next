package next;

public class switchNext {
	
	public static void main(String[] args) {
		
		int mes = 1;
		
		switch(mes) {
		case 1:
			System.out.println("Daniel e Leticia");
			break;
		case 2:
			System.out.println("Daniel e Angela");
			break;
		case 3:
			System.out.println("Emanuelle e Arthur");
			break;
			default:
				System.out.println("Ninguém fez aniversário");
		}
	}
}
